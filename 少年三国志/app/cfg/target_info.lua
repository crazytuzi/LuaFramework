--@return @class record_target_info
function target_info.get(id)
    
    return target_info.indexOf(__index_id[id])
        
end



function target_info.set(id, key, value)
    local record = target_info.get(id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end




