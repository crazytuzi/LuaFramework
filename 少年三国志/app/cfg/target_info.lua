                return o
            end 
        end
        
        assert(__key_map[k], "cannot find " .. k .. " in record_target_info")
        
        
        return t._raw[__key_map[k]]
    end
}


function target_info.getLength()
    return #target_info._data
end



function target_info.hasKey(k)
  if __key_map[k] == nil then
    return false
  else
    return true
  end
end


